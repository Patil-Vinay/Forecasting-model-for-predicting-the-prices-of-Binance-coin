def calculate_prediction_features(price_df):
    """
    Calculate all 14 features from recent price data for making new predictions.

    Parameters:
    -----------
    price_df : pandas.DataFrame
        DataFrame with columns: Date, Price, High, Low, Volume
        Minimum 35 days of data required

    Returns:
    --------
    features : list
        List of 14 features ready for model prediction (in exact order)
    current_price : float
        Today's closing price
    df : pandas.DataFrame
        DataFrame with all calculated features (for inspection)
    """
    df = price_df.copy()

    # Ensure proper date format and sorting
    df['Date'] = pd.to_datetime(df['Date'])
    df = df.sort_values('Date').reset_index(drop=True)

    # Rename to match
    if 'Price' not in df.columns and 'Close' in df.columns:
        df['Price'] = df['Close']

    # Calculate all derived features

    # Features 1-3: Price momentum (percentage changes)
    df['pct_change_1d'] = df['Price'].pct_change(1) * 100
    df['pct_change_2d'] = df['Price'].pct_change(2) * 100
    df['pct_change_7d'] = df['Price'].pct_change(7) * 100

    # Features 4-6: Volatility measures
    df['volatility_7d'] = df['pct_change_1d'].rolling(window=7).std()
    df['volatility_14d'] = df['pct_change_1d'].rolling(window=14).std()
    df['hl_range_pct'] = ((df['High'] - df['Low']) / df['Low']) * 100

    # Calculate moving averages for features 7-9
    df['ma_7'] = df['Price'].rolling(window=7).mean()
    df['ma_14'] = df['Price'].rolling(window=14).mean()
    df['ma_30'] = df['Price'].rolling(window=30).mean()

    # Feature 10: Volume change
    df['volume_change_pct'] = df['Volume'].pct_change(1) * 100

    # Feature 11: RSI calculation
    delta = df['Price'].diff()
    gain = (delta.where(delta > 0, 0)).rolling(window=14).mean()
    loss = (-delta.where(delta < 0, 0)).rolling(window=14).mean()
    rs = gain / loss
    df['rsi_14'] = 100 - (100 / (1 + rs))

    # Extract today's values (last row, index -1)
    current_price = df['Price'].iloc[-1]
    today = df['Date'].iloc[-1]


    features = [
        df['pct_change_1d'].iloc[-1],
        df['pct_change_2d'].iloc[-1],
        df['pct_change_7d'].iloc[-1],
        df['volatility_7d'].iloc[-1],
        df['volatility_14d'].iloc[-1],
        df['hl_range_pct'].iloc[-2],
        (current_price / df['ma_7'].iloc[-1]) * 100,
        (current_price / df['ma_14'].iloc[-1]) * 100,
        (current_price / df['ma_30'].iloc[-1]) * 100,
        df['volume_change_pct'].iloc[-1],
        df['rsi_14'].iloc[-1],
        today.dayofweek,
        today.month,
        (today.month - 1) // 3 + 1
    ]

    return features, current_price, df

def predict_next_day(recent_price_data, trained_model):
    """
    Complete pipeline to predict tomorrow's price.

    Parameters:
    -----------
    recent_price_data : pandas.DataFrame
        Recent price data with columns: Date, Price/Close, High, Low, Volume
        Minimum 35 days required
    trained_model : XGBRegressor
        Your trained XGBoost model

    Returns:
    --------
    dict : Dictionary containing:
        - current_price: Today's closing price
        - predicted_price: Tomorrow's predicted price
        - predicted_change_pct: Predicted percentage change
        - predicted_change_usd: Predicted dollar change
        - signal: Trading signal (BUY/SELL/HOLD)
        - confidence: Signal confidence (HIGH/MEDIUM/LOW)
        - features: Feature vector used (for inspection)
        - rsi: RSI value
        - trend_7d: 7-day price trend
    """
    # Calculate features
    features, current_price, df = calculate_prediction_features(recent_price_data)

    # Make prediction
    features_array = np.array(features).reshape(1, -1)
    predicted_pct_change = trained_model.predict(features_array)[0]

    # Calculate predicted price
    predicted_price = current_price * (1 + predicted_pct_change / 100)
    price_change_usd = predicted_price - current_price

    # Generate trading signal
    if predicted_pct_change > 2.0:
        signal = "BUY"
        confidence = "HIGH" if predicted_pct_change > 3.0 else "MEDIUM"
    elif predicted_pct_change < -2.0:
        signal = "SELL"
        confidence = "HIGH" if predicted_pct_change < -3.0 else "MEDIUM"
    else:
        signal = "HOLD"
        confidence = "LOW"

    # Return results
    return {
        'current_price': current_price,
        'predicted_price': predicted_price,
        'predicted_change_pct': predicted_pct_change,
        'predicted_change_usd': price_change_usd,
        'signal': signal,
        'confidence': confidence,
        'features': features,
        'rsi': features[10],
        'trend_7d': features[2]
    }

