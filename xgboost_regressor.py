"""## 2. Load and Explore Data"""

# Load the historical cryptocurrency data
dataset = pd.read_csv('Binance Coin - Historic data.csv')

# Display basic information
print(f"Dataset Shape: {dataset.shape}")
print(f"\nColumns: {list(dataset.columns)}")
print(f"\nDate Range: {dataset['Date'].iloc[-1]} to {dataset['Date'].iloc[0]}")

# Display first few rows
dataset.head()

"""## 3. Data Preprocessing"""

# Convert volume from string format to numeric
for x in range(dataset['Vol.'].shape[0]):
    val = dataset["Vol."].iloc[x]
    if "M" in val:
        dataset["Vol."].iloc[x] = float(val[:-1]) * 1000000
    elif "K" in val:
        dataset["Vol."].iloc[x] = float(val[:-1]) * 1000

# Convert volume column to numeric type
dataset['Vol.'] = pd.to_numeric(dataset['Vol.'], errors='coerce')

# Convert date column to datetime type
dataset['Date'] = pd.to_datetime(dataset['Date'])

# Sort by date in ascending order
dataset = dataset.sort_values('Date').reset_index(drop=True)

"""## 4. Feature Engineering"""

#  Predict Price change in %
dataset['current_price'] = dataset['Price(in dollars)']
dataset['next_price'] = dataset['Price(in dollars)'].shift(-1)
dataset['Target_pct_change'] = ((dataset['next_price'] - dataset['current_price']) / dataset['current_price']) * 100

# Recent percentage changes
dataset['prev_pct_change_1d'] = dataset['Price(in dollars)'].pct_change(1) * 100
dataset['prev_pct_change_2d'] = dataset['Price(in dollars)'].pct_change(2) * 100
dataset['prev_pct_change_7d'] = dataset['Price(in dollars)'].pct_change(7) * 100

# Measure price fluctuation
dataset['volatility_7d'] = dataset['prev_pct_change_1d'].rolling(window=7).std()
dataset['volatility_14d'] = dataset['prev_pct_change_1d'].rolling(window=14).std()

# High-Low range as percentage (Same Day/Intraday)
dataset['prev_hl_range_pct'] = ((dataset['High'].shift(1) - dataset['Low'].shift(1)) / dataset['Low'].shift(1)) * 100

# Average prices over different time windows
dataset['ma_7'] = dataset['Price(in dollars)'].rolling(window=7).mean()    # 7 day average
dataset['ma_14'] = dataset['Price(in dollars)'].rolling(window=14).mean()  # 14 day average
dataset['ma_30'] = dataset['Price(in dollars)'].rolling(window=30).mean()  # 30 day average

# Price position relative to moving averages (Bullish/Bearish)
dataset['price_to_ma7_ratio'] = (dataset['current_price'] / dataset['ma_7']) * 100
dataset['price_to_ma14_ratio'] = (dataset['current_price'] / dataset['ma_14']) * 100
dataset['price_to_ma30_ratio'] = (dataset['current_price'] / dataset['ma_30']) * 100

# Trading volume changes (high volume confirms price moves)
dataset['volume_change_pct'] = dataset['Vol.'].pct_change(1) * 100

# RSI (Relative Strength Index) - momentum oscillator
# RSI > 70: Overbought (potential sell signal)
# RSI < 30: Oversold (potential buy signal)
delta = dataset['Price(in dollars)'].diff()
gain = (delta.where(delta > 0, 0)).rolling(window=14).mean()
loss = (-delta.where(delta < 0, 0)).rolling(window=14).mean()
rs = gain / loss
dataset['rsi_14'] = 100 - (100 / (1 + rs))

# Temporal patterns
dataset['day_of_week'] = dataset['Date'].dt.dayofweek  # 0=Monday, 6=Sunday
dataset['month'] = dataset['Date'].dt.month            # 1=January, 12=December
dataset['quarter'] = dataset['Date'].dt.quarter        # 1=Q1, 4=Q4

"""## 5. Data Cleaning"""

# Remove rows with NaN values
dataset_clean = dataset.dropna().reset_index(drop=True)

"""## 6. Train-Test Split
Chronological split:
- Training: Earlier 80% of data
- Testing: Later 20% of data
"""

split_idx = int(len(dataset_clean) * 0.8)

train_data = dataset_clean[:split_idx]
test_data = dataset_clean[split_idx:]

print(f"\nTraining Period: {train_data['Date'].iloc[0].strftime('%Y-%m-%d')} to {train_data['Date'].iloc[-1].strftime('%Y-%m-%d')}")
print(f"Testing Period:  {test_data['Date'].iloc[0].strftime('%Y-%m-%d')} to {test_data['Date'].iloc[-1].strftime('%Y-%m-%d')}")
print(f"\nTraining Samples: {len(train_data)} ({len(train_data)/len(dataset_clean)*100:.1f}%)")
print(f"Testing Samples:  {len(test_data)} ({len(test_data)/len(dataset_clean)*100:.1f}%)")
print(f"\nPrice Statistics:")
print(f"  Training - Min: ${train_data['current_price'].min():.2f}, Max: ${train_data['current_price'].max():.2f}, Avg: ${train_data['current_price'].mean():.2f}")
print(f"  Testing  - Min: ${test_data['current_price'].min():.2f}, Max: ${test_data['current_price'].max():.2f}, Avg: ${test_data['current_price'].mean():.2f}")

feature_cols = [
    # Price momentum
    'prev_pct_change_1d', 'prev_pct_change_2d', 'prev_pct_change_7d',
    # Volatility
    'volatility_7d', 'volatility_14d', 'prev_hl_range_pct',
    # Moving average ratios
    'price_to_ma7_ratio', 'price_to_ma14_ratio', 'price_to_ma30_ratio',
    # Volume
    'volume_change_pct',
    # Technical indicators
    'rsi_14',
    # Temporal
    'day_of_week', 'month', 'quarter'
]

# Feature matrices and target vectors
X_train = train_data[feature_cols].values
y_train = train_data['Target_pct_change'].values

X_test = test_data[feature_cols].values
y_test = test_data['Target_pct_change'].values

test_current_prices = test_data['current_price'].values
test_actual_prices = test_data['next_price'].values

"""## 7. Model Training

"""

# Initialize XGBoost Regressor
regressor = XGBRegressor(
    objective='reg:squarederror',
    n_estimators=200,
    learning_rate=0.05,
    max_depth=5,
    min_child_weight=3,
    subsample=0.8,
    colsample_bytree=0.8,
    random_state=42,
    n_jobs=-1
)

# Training the model
regressor.fit(
    X_train,
    y_train,
    eval_set=[(X_train, y_train), (X_test, y_test)],
    verbose=True
)

"""## 8. Make Predictions"""

# Predict percentage changes
y_train_pred_pct = regressor.predict(X_train)
y_test_pred_pct = regressor.predict(X_test)

# Convert predicted percentage changes back to dollar prices
test_pred_prices = test_current_prices * (1 + y_test_pred_pct / 100)

"""## 9. Model Evaluation

**Metrics:**
- **R² Score**: Proportion of variance explained (0-1, higher is better)
- **RMSE**: Root Mean Squared Error in dollars (lower is better)
- **MAE**: Mean Absolute Error in dollars (average prediction error)
- **MAPE**: Mean Absolute Percentage Error (average % error)
"""

